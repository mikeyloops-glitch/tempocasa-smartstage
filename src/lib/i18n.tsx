type LanguageContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  labelRoom: (value: RoomType) => string;
  labelStyle: (value: StagingStyle) => string;
  labelLevel: (value: StagingLevel) => string;
  labelMode: (value: GenerationMode) => string;
  labelStatus: (value: ProjectRecord["status"]) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

function isLanguageCode(value: string | null): value is LanguageCode {
  return languages.some((language) => language.code === value);
}

function interpolate(value: string, vars?: Record<string, string | number>) {
  if (!vars) {
    return value;
  }

  return Object.entries(vars).reduce((next, [key, replacement]) => next.replaceAll(`{${key}}`, String(replacement)), value);
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>("it");

  useEffect(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (isLanguageCode(stored)) {
      setLanguageState(stored);
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<LanguageContextValue>(() => {
    const dictionary = dictionaries[language];

    function t(key: string, vars?: Record<string, string | number>) {
      return interpolate(dictionary[key] ?? dictionaries.en[key] ?? key, vars);
    }

    function setLanguage(nextLanguage: LanguageCode) {
      setLanguageState(nextLanguage);
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    }

    return {
      language,
      setLanguage,
      t,
      labelRoom: (room) => t(`room.${room}`),
      labelStyle: (style) => t(`style.${style}`),
      labelLevel: (level) => t(`level.${level}`),
      labelMode: (mode) => t(`mode.${mode}.label`),
      labelStatus: (status) => t(`status.${status}`),
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const value = useContext(LanguageContext);

  if (!value) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }

  return value;
}
